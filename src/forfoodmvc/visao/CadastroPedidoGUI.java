    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        boolean cadastro;
        Pedido p = new Pedido();
        PedidoControle pC = new PedidoControle();
        //---------------------------------------------------------------------------------
        p.setPedMesa(Integer.parseInt(jComboBoxNumeroMesa.getSelectedItem().toString()));
        //---------------------------------------------------------------------------------
        if (jComboBoxPago.getSelectedItem().equals("Sim")) {
            p.setPedStatus(true);
        } else if (jComboBoxPago.getSelectedItem().equals("Não")) {
            p.setPedStatus(false);
        }
        //---------------------------------------------------------------------------------
        p.setPedValorTotal(Float.parseFloat(jTextFieldValorFinaol.getText()));
        //---------------------------------------------------------------------------------
        //---------------------------------------------------------------------------------
        p.setFuncionario(encontraFuncionario());
        //---------------------------------------------------------------------------------
        p.setPedData(Date.valueOf(dt.format(dateDe2.getDate())));
        //---------------------------------------------------------------------------------
        p.setPedQuantidade(Integer.parseInt((String) jComboBoxQuantidade.getSelectedItem()));
        //---------------------------------------------------------------------------------
        p.setPratos(preencherPratos(codPratos));
        //---------------------------------------------------------------------------------
        pC.cadastrarControle(p);
        cadastro = true;
        JOptionPane.showMessageDialog(null, " Cadastro realizado com sucesso!");
        listar();

        modelo.addElement(null);
        jList1.setModel(modelo);

    }//GEN-LAST:event_jButtonCadastrarActionPerformed

