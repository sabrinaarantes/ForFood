    private void jButton1InserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1InserirActionPerformed

        PratoControle pc = new PratoControle();
        List<Prato> arrP = null;
        arrP = pc.listarPratoControle();

        String linhaCombo = (String) jComboBoxPrato.getSelectedItem();
        String[] parts = linhaCombo.split("-");
        int cod = Integer.parseInt(parts[0]);
        System.out.println("SPLIT" + "\n=====" + parts[1] + "=====");

        float preco = 0;
        for (Prato arrP1 : arrP) {
            if (arrP1.getPraCodigo() == cod) {
                float precoAntigo = Float.parseFloat(jTextFieldValorFinaol.getText());
                preco = arrP1.getPraPreco();
                float quantidade = Float.parseFloat((String) jComboBoxQuantidade.getSelectedItem());
                float novoPreco = precoAntigo + (preco * quantidade);
                jTextFieldValorFinaol.setText(String.valueOf(novoPreco));
                codPratos.add(cod);
                //System.out.println("===============" + codPratos.size());
                break;
            }
        }
        modelo.addElement(linhaCombo);
        jList1.setModel(modelo);
    }//GEN-LAST:event_jButton1InserirActionPerformed

